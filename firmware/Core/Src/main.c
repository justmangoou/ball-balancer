/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
  #if !defined(__FPU_USED) || (__FPU_USED != 1)
  #error "FPU is NOT enabled in the compiler! Check CMake flags."
  #endif

  SCB->CPACR |= ((3UL << 10*2) | (3UL << 11*2));
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  DWT_Init();
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_TIM5_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
  HAL_TIM_Base_Start_IT(&htim5);
  Touch_Init();
  Controller_Init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  static Touch_RawPoint raw_point = { 0 };
  static Touch_CenterOffsetPercentage ball_offset = { 0 };

  while (1)
  {
    if (heartbeat_tick) {
      heartbeat_tick = false;

      if (Touch_Scan(&raw_point))
      {
        ball_detected = true;
        miss_counter = 0;

        Touch_CenterOffsetPercent(&raw_point, &ball_offset);

        Controller_Update(&ball_offset);
      }
      else if (++miss_counter >= MISS_THRESHOLD)
      {
        ball_detected = false;
        ball_offset.x = 0;
        ball_offset.y = 0;
        Controller_Reset();

        miss_counter = MISS_THRESHOLD;
      }

      #ifndef NDEBUG
      x_raw = raw_point.x;
      y_raw = raw_point.y;
      x_parc = ball_offset.x;
      y_parc = ball_offset.y;
      pressure = raw_point.z;
      #endif
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

